#!/bin/bash
# ================================================================================
# 脚本名称：部署配置.sh
# 功能描述：将站点配置从管理目录部署到运行目录
# 适用环境：宝塔面板
# 创建日期：2026-05-20
#
# 设计思路：
# 1. 区分"工作目录"和"运行目录"的原因：
#    - 工作目录：GoAccess-管理/站点配置（用于编辑）
#    - 运行目录：运行配置（仅放生效的配置）
# 2. 支持预览模式（--dry-run），方便确认不会出错
# 3. 支持强制模式（--force），方便自动化
# 4. 友好的确认提示和帮助信息
# ================================================================================

# 开启严格错误处理模式
set -eo pipefail

# ================================================================================
# 常量定义区域（使用 readonly 确保常量不可修改）
# ================================================================================
readonly SCRIPT_NAME="$(basename "$0")"              # 脚本文件名
readonly SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)" # 脚本所在目录（绝对路径）
readonly CONFIG_SOURCE="$SCRIPT_DIR/站点配置"       # 配置源目录（工作区）
readonly CONFIG_DEST="/www/wwwroot/运行配置"        # 配置目标目录（运行区）

# ================================================================================
# ANSI 颜色代码定义（用于美化输出）
# ================================================================================
readonly RED='\033[0;31m'       # 红色（错误信息）
readonly GREEN='\033[0;32m'     # 绿色（成功信息）
readonly YELLOW='\033[1;33m'    # 黄色（警告/信息）
readonly BLUE='\033[0;34m'      # 蓝色（标题/分隔线）
readonly CYAN='\033[0;36m'      # 青色（提示信息）
readonly NC='\033[0m'           # 恢复默认颜色

# ================================================================================
# 全局变量（注意：这些不是 readonly，因为会被命令行参数修改）
# ================================================================================
DRY_RUN=false  # 是否预览模式（只显示不执行）
FORCE=false    # 是否强制模式（跳过确认）

# ================================================================================
# 工具函数库（通用操作封装）
# ================================================================================

# --------------------------------------------------------------------------------
# print_separator: 打印蓝色分隔线
# 用途：美化输出，区分不同的操作阶段
# --------------------------------------------------------------------------------
print_separator() {
    echo -e "${BLUE}============================================================${NC}"
}

# --------------------------------------------------------------------------------
# print_title: 打印带边框的标题
# 参数：$1 - 标题内容
# 用途：突出显示重要阶段
# --------------------------------------------------------------------------------
print_title() {
    print_separator
    echo -e "${GREEN}$1${NC}"
    print_separator
    echo ""
}

# --------------------------------------------------------------------------------
# log_info: 打印信息级别的日志（黄色）
# 参数：$1 - 信息内容
# --------------------------------------------------------------------------------
log_info() {
    echo -e "${YELLOW}[INFO] $1${NC}"
}

# --------------------------------------------------------------------------------
# log_success: 打印成功级别的日志（绿色）
# 参数：$1 - 成功信息
# --------------------------------------------------------------------------------
log_success() {
    echo -e "${GREEN}[OK] $1${NC}"
}

# --------------------------------------------------------------------------------
# log_error: 打印错误级别的日志（红色，输出到 stderr）
# 参数：$1 - 错误信息
# --------------------------------------------------------------------------------
log_error() {
    echo -e "${RED}[ERROR] $1${NC}" >&2
}

# --------------------------------------------------------------------------------
# log_warning: 打印警告级别的日志（黄色）
# 参数：$1 - 警告信息
# --------------------------------------------------------------------------------
log_warning() {
    echo -e "${YELLOW}[WARNING] $1${NC}"
}

# --------------------------------------------------------------------------------
# print_config_list: 输出配置文件列表
# 参数：$1 - 配置数量（可选，仅用于显示）
# 用途：列出将要部署的配置文件
# --------------------------------------------------------------------------------
print_config_list() {
    local count=$1
    echo -e "  ${CYAN}配置文件列表:${NC}"
    local i=1
    for file in "$CONFIG_SOURCE"/*.conf; do
        if [ -f "$file" ]; then
            local basename=$(basename "$file")
            echo -e "    $i. $basename"
            i=$((i + 1))
        fi
    done
    echo ""
}

# --------------------------------------------------------------------------------
# confirm_action: 交互式确认操作
# 参数：$1 - 提示信息，$2 - 默认值（可选，默认 N）
# 返回：0 - 确认，1 - 取消
# 设计说明：
# - 如果是 FORCE 模式，直接返回确认
# - 如果是 DRY_RUN 模式，直接返回确认
# - 否则显示 [Y/n] 或 [y/N] 提示
# --------------------------------------------------------------------------------
confirm_action() {
    local prompt="$1"
    local default="${2:-N}"

    # 如果是强制模式，不需要确认
    if [ "$FORCE" = true ]; then
        return 0
    fi

    # 如果是预览模式，不需要确认
    if [ "$DRY_RUN" = true ]; then
        return 0
    fi

    # 构建提示信息（显示默认值）
    if [ "$default" = "Y" ]; then
        prompt="$prompt [Y/n]"
    else
        prompt="$prompt [y/N]"
    fi

    # 显示提示并读取输入
    echo -ne "$prompt: "
    read -r response

    # 如果没输入，使用默认值
    if [ -z "$response" ]; then
        response="$default"
    fi

    # 检查是否是 yes（不区分大小写）
    [[ "$response" =~ ^[Yy]$ ]]
}

# --------------------------------------------------------------------------------
# deploy_configs: 执行配置部署（或预览）
# 参数：$1 - 模式（"preview" 或 "deploy"）
# 返回：部署的文件数量
# 设计说明：
# - preview 模式：只显示将要做什么，不实际执行
# - deploy 模式：实际复制文件
# - 区分"新增"和"更新"（目标文件是否存在）
# --------------------------------------------------------------------------------
deploy_configs() {
    local deploy_mode="$1"
    local count=0

    # 预览模式的提示
    if [ "$deploy_mode" = "preview" ]; then
        log_info "预览模式 - 仅显示将要执行的操作"
        echo ""
    fi

    # 遍历源目录中的所有配置文件
    for file in "$CONFIG_SOURCE"/*.conf; do
        if [ ! -f "$file" ]; then
            continue
        fi

        local basename=$(basename "$file")
        local dest_file="$CONFIG_DEST/$basename"

        # 根据模式执行不同操作
        if [ "$deploy_mode" = "preview" ]; then
            # 预览模式：只显示
            if [ -f "$dest_file" ]; then
                echo -e "  ${YELLOW}更新:${NC} $basename"
            else
                echo -e "  ${GREEN}新增:${NC} $basename"
            fi
        else
            # 部署模式：实际复制
            if [ -f "$dest_file" ]; then
                echo -e "  ${YELLOW}更新:${NC} $basename"
            else
                echo -e "  ${GREEN}新增:${NC} $basename"
            fi
            cp -f "$file" "$dest_file"  # -f = 强制覆盖
        fi

        count=$((count + 1))
    done

    return $count
}

# --------------------------------------------------------------------------------
# print_usage: 打印帮助信息
# 用途：显示脚本使用说明
# --------------------------------------------------------------------------------
print_usage() {
    cat << EOF
$SCRIPT_NAME - GoAccess 配置部署工具

用法: $SCRIPT_NAME [选项]

选项:
    -h, --help      显示此帮助信息
    -n, --dry-run   预览模式，仅显示将要执行的操作
    -f, --force     跳过确认提示，直接执行
    -p, --preview   仅预览，不执行部署（与 --dry-run 相同）

示例:
    $SCRIPT_NAME            # 交互式部署
    $SCRIPT_NAME --dry-run  # 预览将要部署的配置
    $SCRIPT_NAME --force    # 无需确认直接部署（适合自动化）

EOF
}

# ================================================================================
# 解析命令行参数
# 设计说明：
# - 使用 while 循环和 case 处理参数
# - shift 将已处理的参数从列表中移除
# ================================================================================

while [[ $# -gt 0 ]]; do
    case "$1" in
        -h|--help)
            print_usage
            exit 0
            ;;
        -n|--dry-run|-p|--preview)
            DRY_RUN=true
            shift
            ;;
        -f|--force)
            FORCE=true
            shift
            ;;
        *)
            log_error "未知选项: $1"
            print_usage
            exit 1
            ;;
    esac
done

# ================================================================================
# 阶段 0：检查运行权限
# 说明：部署配置需要 root 权限来写入运行目录
# ================================================================================
log_info "检查运行权限..."
if [ "$EUID" -ne 0 ]; then
    log_error "请使用 root 权限运行此脚本"
    echo "使用方法：sudo $0"
    exit 1
fi
log_success "权限检查通过"
echo ""

# ================================================================================
# 主程序开始
# ================================================================================

print_title "GoAccess 配置部署工具 v1.1"

# 输出基本信息
log_info "脚本目录: $SCRIPT_DIR"
log_info "源目录:   $CONFIG_SOURCE"
log_info "目标目录: $CONFIG_DEST"
echo ""

# 检查源目录是否存在
if [ ! -d "$CONFIG_SOURCE" ]; then
    log_error "源目录不存在: $CONFIG_SOURCE"
    echo ""
    echo -e "${CYAN}提示：${NC}"
    echo "请先在 $CONFIG_SOURCE 目录中创建配置文件"
    echo "可以复制 $CONFIG_SOURCE/配置模板.conf 进行修改"
    exit 1
fi

# 统计配置文件数量
CONFIG_COUNT=$(ls -1 "$CONFIG_SOURCE"/*.conf 2>/dev/null | wc -l)

# 检查是否有配置文件
if [ "$CONFIG_COUNT" -eq 0 ]; then
    log_warning "源目录中没有找到配置文件"
    echo ""
    echo -e "${CYAN}提示：${NC}"
    echo "请先复制配置模板并编辑："
    echo "  cd $CONFIG_SOURCE"
    echo "  cp ../配置模板.conf 我的网站.conf"
    echo "  nano 我的网站.conf"
    exit 0
fi

# 显示找到的配置文件
echo -e "${GREEN}找到 $CONFIG_COUNT 个配置文件${NC}"
print_config_list "$CONFIG_COUNT"

# 如果是预览模式，只预览然后退出
if [ "$DRY_RUN" = true ]; then
    deploy_configs "preview"
    echo ""
    log_success "预览完成"
    log_info "使用不带 --dry-run 选项运行以执行实际部署"
    exit 0
fi

# 确认操作
echo ""
if confirm_action "确认将这些配置部署到运行目录"; then
    echo ""
    log_info "开始部署配置..."

    # 确保目标目录存在
    mkdir -p "$CONFIG_DEST"

    # 执行部署
    deploy_configs "deploy"

    # 完成提示
    echo ""
    log_success "部署完成！"
    echo ""
    echo -e "${CYAN}下一步操作：${NC}"
    echo "1. 检查运行目录中的配置: ls $CONFIG_DEST"
    echo "2. 运行 ./分析所有站点.sh 生成报告"
    echo ""
else
    # 用户取消了
    echo ""
    log_warning "已取消部署"
    exit 0
fi
