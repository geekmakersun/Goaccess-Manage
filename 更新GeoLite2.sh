#!/bin/bash
# ================================================================================
# 脚本名称：更新GeoLite2.sh
# 功能描述：更新免费版 GeoLite2 地理位置数据库
# 适用环境：宝塔面板 + CentOS/Rocky/AlmaLinux/Debian/Ubuntu
# 创建日期：2026-05-20
#
# 设计思路：
# 1. 使用"原子更新"策略：先下载到临时文件，验证成功后再替换
# 2. 自动备份旧版本（带时间戳）
# 3. 支持重试机制，网络不稳定也能成功
# 4. 验证下载的文件大小，防止下载到错误文件
# 5. 检查磁盘空间，防止下载一半失败
# ================================================================================

# 开启严格错误处理模式
set -eo pipefail

# ================================================================================
# 常量定义区域（使用 readonly 确保常量不可修改）
# ================================================================================
readonly SCRIPT_NAME="$(basename "$0")"              # 脚本文件名
readonly SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)" # 脚本所在目录（绝对路径）
readonly GEOIP_DIR="/usr/share/GeoIP"                # GeoIP 数据库存放目录
readonly GEOIP_URL="https://github.com/P3TERX/GeoLite.mmdb/raw/download/GeoLite2-City.mmdb"  # 免费下载地址
readonly GEOIP_FILE="$GEOIP_DIR/GeoLite2-City.mmdb"  # 最终的数据库文件
readonly TIMEOUT=120                                  # 下载超时时间（秒）
readonly MAX_RETRIES=3                                # 最大重试次数
readonly MIN_DISK_SPACE_MB=100                        # 需要的最小磁盘空间（MB）

# ================================================================================
# ANSI 颜色代码定义（用于美化输出）
# ================================================================================
readonly RED='\033[0;31m'       # 红色（错误信息）
readonly GREEN='\033[0;32m'     # 绿色（成功信息）
readonly YELLOW='\033[1;33m'    # 黄色（警告/信息）
readonly BLUE='\033[0;34m'      # 蓝色（标题/分隔线）
readonly CYAN='\033[0;36m'      # 青色（提示信息）
readonly NC='\033[0m'           # 恢复默认颜色

# ================================================================================
# 工具函数库（通用操作封装）
# ================================================================================

# --------------------------------------------------------------------------------
# print_separator: 打印蓝色分隔线
# 用途：美化输出，区分不同的操作阶段
# --------------------------------------------------------------------------------
print_separator() {
    echo -e "${BLUE}============================================================${NC}"
}

# --------------------------------------------------------------------------------
# print_title: 打印带边框的标题
# 参数：$1 - 标题内容
# 用途：突出显示重要阶段
# --------------------------------------------------------------------------------
print_title() {
    print_separator
    echo -e "${GREEN}$1${NC}"
    print_separator
    echo ""
}

# --------------------------------------------------------------------------------
# log_info: 打印信息级别的日志（黄色）
# 参数：$1 - 信息内容
# --------------------------------------------------------------------------------
log_info() {
    echo -e "${YELLOW}[INFO] $1${NC}"
}

# --------------------------------------------------------------------------------
# log_success: 打印成功级别的日志（绿色）
# 参数：$1 - 成功信息
# --------------------------------------------------------------------------------
log_success() {
    echo -e "${GREEN}[OK] $1${NC}"
}

# --------------------------------------------------------------------------------
# log_error: 打印错误级别的日志（红色，输出到 stderr）
# 参数：$1 - 错误信息
# --------------------------------------------------------------------------------
log_error() {
    echo -e "${RED}[ERROR] $1${NC}" >&2
}

# --------------------------------------------------------------------------------
# log_warning: 打印警告级别的日志（黄色）
# 参数：$1 - 警告信息
# --------------------------------------------------------------------------------
log_warning() {
    echo -e "${YELLOW}[WARNING] $1${NC}"
}

# --------------------------------------------------------------------------------
# check_disk_space: 检查磁盘空间
# 参数：$1 - 需要的空间（MB）
# 返回：0 - 足够，1 - 不足
# 设计说明：
# - 优先检查目标目录所在磁盘
# - 如果目标目录不存在，检查 /tmp 目录
# --------------------------------------------------------------------------------
check_disk_space() {
    local required_mb=$1
    # 先检查目标目录所在的磁盘
    local available_mb=$(df -m "$GEOIP_DIR" 2>/dev/null | awk 'NR==2 {print $4}')

    # 如果目标目录不存在，使用 /tmp 目录
    if [ -z "$available_mb" ]; then
        available_mb=$(df -m /tmp | awk 'NR==2 {print $4}')
    fi

    if [ "$available_mb" -lt "$required_mb" ]; then
        log_error "磁盘空间不足，需要 ${required_mb}MB，可用 ${available_mb}MB"
        return 1
    fi
    return 0
}

# --------------------------------------------------------------------------------
# download_with_retry: 带重试的下载函数
# 参数：$1 - 下载地址，$2 - 保存路径
# 返回：0 - 成功，1 - 失败
# 设计说明：
# - 使用 wget 下载，设置超时和重试参数
# - 失败后会等待 TIMEOUT 秒再重试
# --------------------------------------------------------------------------------
download_with_retry() {
    local url=$1
    local output=$2
    local attempt=1

    while [ $attempt -le $MAX_RETRIES ]; do
        log_info "下载尝试 ${attempt}/${MAX_RETRIES}..."

        # wget 参数说明：
        # --timeout：连接超时时间
        # --tries：单次尝试的重试次数
        # --waitretry：重试之间等待的时间（秒）
        # -O：指定输出文件
        if wget --timeout="$TIMEOUT" \
               --tries=3 \
               --waitretry=5 \
               -O "$output" \
               "$url" 2>/dev/null; then
            return 0
        fi

        log_warning "下载失败，${TIMEOUT}秒后重试..."
        sleep "$TIMEOUT"
        attempt=$((attempt + 1))
    done

    return 1
}

# --------------------------------------------------------------------------------
# atomic_update: 原子性更新文件
# 参数：$1 - 新文件（临时文件），$2 - 旧文件（目标文件）
# 返回：0 - 成功，1 - 失败
# 设计思路（关键）：
# 1. 先备份旧文件（如果存在），带时间戳
# 2. 验证新文件：不能是空文件，大小至少要合理
# 3. 使用 mv 原子性替换（mv 在同一个文件系统是原子操作）
# 4. 如果任何步骤失败，保持旧文件不变
# --------------------------------------------------------------------------------
atomic_update() {
    local new_file=$1
    local old_file=$2
    # 备份文件名格式：原文件名.backup.年月日_时分秒
    local backup_file="${old_file}.backup.$(date +%Y%m%d_%H%M%S)"

    # 步骤 1：备份旧文件（如果存在）
    if [ -f "$old_file" ]; then
        log_info "备份当前数据库到: $backup_file"
        if ! cp "$old_file" "$backup_file"; then
            log_error "备份失败"
            return 1
        fi
    fi

    # 步骤 2：验证新文件是否有效
    log_info "验证下载的文件..."
    # -s 检查文件是否存在且大小不为 0
    if [ ! -s "$new_file" ]; then
        log_error "下载的文件为空或大小为 0"
        rm -f "$new_file"
        return 1
    fi

    # 获取文件大小（兼容不同系统）
    # stat -c%s：Linux 系统
    # stat -f%z：macOS 系统（备用方案）
    local file_size=$(stat -c%s "$new_file" 2>/dev/null || stat -f%z "$new_file" 2>/dev/null)
    # 文件大小至少要大于 1MB（GeoLite2-City.mmdb 通常 50-100MB）
    if [ "$file_size" -lt 1000000 ]; then
        log_error "文件大小异常: ${file_size} bytes，可能下载失败"
        rm -f "$new_file"
        return 1
    fi

    # 步骤 3：原子性替换（mv 命令在同文件系统是原子操作）
    log_info "移动新文件到目标位置..."
    if ! mv "$new_file" "$old_file"; then
        log_error "移动文件失败"
        return 1
    fi

    log_success "原子更新完成"
    return 0
}

# ================================================================================
# 主程序开始
# ================================================================================

print_title "GeoLite2 数据库更新脚本 v1.1"

log_info "脚本目录: $SCRIPT_DIR"
log_info "目标位置: $GEOIP_FILE"
echo ""

# --------------------------------------------------------------------------------
# 阶段 1：检查运行权限
# 设计说明：更新系统目录需要 root 权限
# --------------------------------------------------------------------------------
log_info "检查运行权限..."
if [ "$EUID" -ne 0 ]; then
    log_error "请使用 root 权限运行此脚本"
    echo "使用方法：sudo $0"
    exit 1
fi
log_success "权限检查通过"
echo ""

# --------------------------------------------------------------------------------
# 阶段 2：创建目录
# 设计说明：
# - mkdir -p：如果目录不存在就创建，存在也不会报错
# - 检查创建是否成功（虽然 mkdir -p 通常不会失败）
# --------------------------------------------------------------------------------
log_info "确保目录存在..."
mkdir -p "$GEOIP_DIR"
if [ ! -d "$GEOIP_DIR" ]; then
    log_error "无法创建目录: $GEOIP_DIR"
    exit 1
fi
log_success "目录就绪"
echo ""

# --------------------------------------------------------------------------------
# 阶段 3：检查磁盘空间
# 设计说明：防止下载到一半磁盘满了
# --------------------------------------------------------------------------------
log_info "检查磁盘空间..."
check_disk_space "$MIN_DISK_SPACE_MB"
echo ""

# --------------------------------------------------------------------------------
# 阶段 4：下载新数据库
# 设计说明：
# 1. 先显示当前数据库大小（如果有）
# 2. 下载到临时文件（带时间戳，避免冲突）
# 3. 使用带重试的下载函数
# --------------------------------------------------------------------------------
log_info "下载地址: $GEOIP_URL"

# 如果已有数据库，显示当前大小
if [ -f "$GEOIP_FILE" ]; then
    current_size=$(stat -c%s "$GEOIP_FILE" 2>/dev/null || stat -f%z "$GEOIP_FILE" 2>/dev/null)
    log_info "当前数据库大小: ${current_size} bytes"
fi

# 临时文件名：原文件名.tmp.时间戳
TEMP_FILE="${GEOIP_FILE}.tmp.$(date +%s)"

log_info "开始下载..."
if download_with_retry "$GEOIP_URL" "$TEMP_FILE"; then
    log_success "下载完成"
else
    log_error "下载失败，已重试 $MAX_RETRIES 次"
    rm -f "$TEMP_FILE"
    exit 1
fi
echo ""

# --------------------------------------------------------------------------------
# 阶段 5：原子性更新
# 设计说明：
# 1. 调用 atomic_update 函数
# 2. 如果成功，显示完成信息
# 3. 给出下一步建议
# --------------------------------------------------------------------------------
log_info "执行原子更新..."
if atomic_update "$TEMP_FILE" "$GEOIP_FILE"; then
    echo ""
    print_title "更新完成！"

    log_success "GeoLite2 数据库已成功更新！"
    echo ""

    # 显示新数据库信息
    if [ -f "$GEOIP_FILE" ]; then
        new_size=$(stat -c%s "$GEOIP_FILE" 2>/dev/null || stat -f%z "$GEOIP_FILE" 2>/dev/null)
        echo -e "  ${BLUE}文件大小:${NC} ${new_size} bytes"
        echo -e "  ${BLUE}文件位置:${NC} $GEOIP_FILE"
    fi

    echo ""
    echo -e "${CYAN}下一步：${NC}"
    echo "1. 运行 ./分析所有站点.sh 生成新的访问报告"
    echo "2. 或在宝塔面板设置定时任务自动更新（每月 1 号）"
    echo ""
else
    log_error "更新失败"
    exit 1
fi
